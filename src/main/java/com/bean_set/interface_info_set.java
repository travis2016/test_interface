package com.bean_set;

/**
 * 接口列表
 */

public class interface_info_set {
    public int interface_id;
    public String interface_name;
    public String methods;
    public String get_param;
    public String post_param;
    public String interface_addtime;
    public String add_author;
    public String interface_updatetime;
    public int interface_status;


    public int getInterface_id() {
        return interface_id;
    }

    public void setInterface_id(int interface_id) {
        this.interface_id = interface_id;
    }

    public String getInterface_name() {
        return interface_name;
    }

    public void setInterface_name(String interface_name) {
        this.interface_name = interface_name;
    }

    public String getMethods() {
        return methods;
    }

    public void setMethods(String methods) {
        this.methods = methods;
    }

    public String getGet_param() {
        return get_param;
    }

    public void setGet_param(String get_param) {
        this.get_param = get_param;
    }

    public String getPost_param() {
        return post_param;
    }

    public void setPost_param(String post_param) {
        this.post_param = post_param;
    }

    public String getInterface_addtime() {
        return interface_addtime;
    }

    public void setInterface_addtime(String interface_addtime) {
        this.interface_addtime = interface_addtime;
    }

    public String getAdd_author() {
        return add_author;
    }

    public void setAdd_author(String add_author) {
        this.add_author = add_author;
    }

    public String getInterface_updatetime() {
        return interface_updatetime;
    }

    public void setInterface_updatetime(String interface_updatetime) {
        this.interface_updatetime = interface_updatetime;
    }

    public int getInterface_status() {
        return interface_status;
    }

    public void setInterface_status(int interface_status) {
        this.interface_status = interface_status;
    }
}




